    <h1>Receita: Bolo de cenoura com cobertura perfeito.</h1> 

    <p>Venha aprender a fazer o bolo de cenoura com cobertura perfeito para adoçar seus cafés e momentos especiais em família!</p> 
  
  
    <h2>Ingredientes:</h2>
        <ul><li> 1/2 xícara (chá) de óleo;</li></ul>
        <ul><li>4 ovos;</li></ul>
        <ul><li>2 e 1/2 xícaras (chá) de farinha de trigo;</li></ul>
        <ul><li>3 cenouras médias raladas;</li></ul>
        <ul><li>2 xícaras (chá) de açúcar;</li></ul>
        <ul><li>1 colher (sopa) de fermento em pó.</li></ul>
    
    <h2>Cobertura (ingredientes):</h2>
        <ul><li>1 colher (sopa) de manteiga;</li></ul>
        <ul><li>1 xícara (chá) de açúcar;</li></ul>
        <ul><li>3 colheres (sopa) de chocolate em pó;</li></ul>
        <ul><li>1 xícara (chá) de leite.</li></ul>
    
        <h2>Modo de Preparo:</h2>
    <ol>
        <li>Massa: Em um liquidificador, adicione a cenoura, os ovos e o óleo, depois misture.</li>
        <li>O açúcar e bata novamente por 5 minutos.
        <li>Em uma tigela ou na batedeira, adicione a farinha de trigo e depois misture novamente.</li>
        <li>Acrescente o fermento e misture lentamente com uma colher.</li>
        <li>Asse em um forno preaquecido a 180° C por aproximadamente 40 minutos.</li>
        <li>Cobertura: Despeje em uma tigela a manteiga, o chocolate em pó, o açúcar e o leite, depois misture.</li>
        <li>Leve a mistura ao fogo e continue misturando até obter uma consistência cremosa, depois despeje a calda por cima do bolo.</li>
